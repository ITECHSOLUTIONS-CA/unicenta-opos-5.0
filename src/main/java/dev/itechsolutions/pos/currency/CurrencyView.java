    @Override
    public void writeValueEOF() {
        m_oId = null;
        jISOCodeText.setText(null);
        jNameText.setText(null);
    }
    
    @Override
    public void writeValueInsert() {
        m_oId = null;
        jISOCodeText.setText(null);
        jNameText.setText(null);
        
        repaint();
        refresh();
    }
    
    @Override
    public void writeValueEdit(Object value) {
        Object[] currency = (Object[]) value;
        
        m_oId = currency[0];
        jNameText.setText(Formats.STRING.formatValue(currency[1]));
        jISOCodeText.setText(Formats.STRING.formatValue(currency[2]));
        
        repaint();
        refresh();
    }
    
    @Override
    public void writeValueDelete(Object value) {
        Object[] currency = (Object[]) value;
        
        m_oId = currency[0];
        
        jNameText.setText(Formats.STRING.formatValue(currency[1]));
        jISOCodeText.setText(Formats.STRING.formatValue(currency[2]));
        
        repaint();
        refresh();
    }
    
    @Override
    public void refresh() {
        
    }
    
    @Override
    public Component getComponent() {
        return this;
    }
    
    @Override
    public Object createValue() throws BasicException {
        Object[] currency = new Object[3];
        
        currency[0] = m_oId == null ? UUID.randomUUID().toString() : m_oId;
        currency[1] = Formats.STRING.parseValue(jNameText.getText());
        currency[2] = Formats.STRING.parseValue(jISOCodeText.getText());
        
        return currency;
    }
    
